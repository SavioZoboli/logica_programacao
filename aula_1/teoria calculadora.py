# Pedir dois números para o usuário
numero1 = float(input("Digite um número: "))
numero2 = float(input("Digite mais um numero: "))

# Operações matemáticas
# - + adição (soma dois valores)
# - - subtração (subtrai o valor b do valor a)
# - * multiplicação (multiplica o valor a pelo b)
# - / divisão (divide o valor a pelo valor b)
# - ** potenciação (eleva o valor a pelo b)
# - % resto de divisão (pega o resto de divisão com resultado inteiro)

print("O resto de divisão entre os valores é ",numero1 % numero2)